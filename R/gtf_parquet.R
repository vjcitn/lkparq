#' Extract genomic features from a GTFParquet object
#'
#' @name genes
#' @aliases genes,GTFParquet-method
#' @aliases transcripts,GTFParquet-method
#' @aliases exons,GTFParquet-method
#' @aliases cds,GTFParquet-method
#'
#' @description
#' Methods to extract genomic features from a GTFParquet object as GRanges.
#' Unlike TxDb methods, these preserve all GTF attributes as metadata columns.
#'
#' @usage
#' \S4method{genes}{GTFParquet}(x, columns=NULL, filter=NULL, use_versioned_ids=FALSE)
#'
#' \S4method{transcripts}{GTFParquet}(x, columns=NULL, filter=NULL, use_versioned_ids=FALSE)
#'
#' \S4method{exons}{GTFParquet}(x, columns=NULL, filter=NULL, use_versioned_ids=FALSE)
#'
#' \S4method{cds}{GTFParquet}(x, columns=NULL, filter=NULL)
#'
#' @param x A \code{\link{GTFParquet}} object.
#' @param columns Character vector of columns to include in \code{mcols}.
#'   If \code{NULL} (default), includes all available attribute columns.
#'   For genes: \code{gene_name}, \code{gene_type}, \code{source}, \code{level}, 
#'   \code{tags}, \code{havana_gene}.
#'   For transcripts: \code{transcript_name}, \code{transcript_type}, \code{gene_id},
#'   \code{gene_name}, \code{transcript_support_level}, \code{ccdsid}, \code{protein_id}.
#' @param filter Optional named list for filtering features.
#'   Names should be column names, values are vectors of acceptable values.
#'   Example: \code{filter = list(gene_type = "protein_coding", chrom = "chr1")}
#' @param use_versioned_ids Logical. If \code{TRUE}, use full versioned IDs
#'   (e.g., \code{ENSG00000141510.18}). If \code{FALSE} (default), use stripped IDs
#'   (e.g., \code{ENSG00000141510}).
#'
#' @details
#' These methods return \link[GenomicRanges]{GRanges} objects with feature IDs
#' as names and rich metadata columns from the original GTF file.
#'
#' The \code{filter} argument enables efficient server-side filtering through
#' Arrow/Parquet predicate pushdown, which can dramatically improve performance
#' compared to subsetting after loading.
#'
#' Available filter columns include:
#' \itemize{
#'   \item \code{chrom}: Chromosome name
#'   \item \code{gene_type}: Gene biotype (e.g., "protein_coding", "lncRNA")
#'   \item \code{transcript_type}: Transcript biotype
#'   \item \code{level}: Annotation confidence (1=verified, 2=manual, 3=automatic)
#'   \item \code{source}: Annotation source ("HAVANA", "ENSEMBL")
#' }
#'
#' @return
#' A \link[GenomicRanges]{GRanges} object with:
#' \itemize{
#'   \item Feature IDs as \code{names}
#'   \item Genomic coordinates (\code{seqnames}, \code{ranges}, \code{strand})
#'   \item Genome build in \code{seqinfo} (e.g., "GRCh38")
#'   \item Rich metadata in \code{mcols}
#' }
#'
#' @seealso
#' \itemize{
#'   \item \code{\link{GTFParquet-class}} for the class definition
#'   \item \code{\link{transcriptsBy,GTFParquet-method}} for grouped extraction
#'   \item \code{\link[GenomicFeatures]{genes}} for the generic
#' }
#'
#' @examples
#' \dontrun{
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#'
#' # Extract all genes with full attributes
#' gr <- genes(gtf)
#' mcols(gr)  # gene_name, gene_type, level, tags, source, havana_gene
#'
#' # Filter by gene type
#' pc <- genes(gtf, filter = list(gene_type = "protein_coding"))
#' lnc <- genes(gtf, filter = list(gene_type = "lncRNA"))
#'
#' # Combine filters
#' pc_chr1 <- genes(gtf, filter = list(gene_type = "protein_coding", chrom = "chr1"))
#'
#' # Select specific columns only
#' gr <- genes(gtf, columns = c("gene_name", "gene_type"))
#'
#' # Use versioned IDs
#' gr <- genes(gtf, use_versioned_ids = TRUE)
#' names(gr)[1]  # "ENSG00000141510.18"
#'
#' # Transcripts with support level
#' tx <- transcripts(gtf)
#' high_conf <- tx[mcols(tx)$transcript_support_level == "1"]
#'
#' # Exons
#' ex <- exons(gtf, filter = list(chrom = "chr1"))
#'
#' # CDS with protein IDs
#' cds_gr <- cds(gtf)
#' mcols(cds_gr)$protein_id
#' }
#'
#' @rdname genes
#' @export
setMethod("genes", "GTFParquet", function(x, columns = NULL, filter = NULL, 
                                           use_versioned_ids = FALSE) {
  if (!x@available[["genes"]]) {
    stop("genes.parquet not found in: ", x@path)
  }
  
  default_cols <- c("gene_name", "gene_type", "source", "level", "tags", "havana_gene")
  if (is.null(columns)) {
    columns <- default_cols
  }
  
  id_col <- if (use_versioned_ids) "gene_id" else "gene_id_stripped"
  required_cols <- c(id_col, "chrom", "start", "end", "strand")
  select_cols <- unique(c(required_cols, columns))
  
  if (!is.null(filter)) {
    select_cols <- unique(c(select_cols, names(filter)))
  }
  
  ds <- arrow::open_dataset(x@files$genes)
  query <- ds |> dplyr::select(dplyr::any_of(select_cols))
  
  if (!is.null(filter)) {
    for (col_name in names(filter)) {
      query <- query |> dplyr::filter(.data[[col_name]] %in% filter[[col_name]])
    }
  }
  
  df <- query |> dplyr::collect() |> dplyr::arrange(chrom, start)
  
  .df_to_granges_rich(df, id_col = id_col, mcol_cols = columns, genome = x@.genome)
})


