# GenomicFeatures::exonsBy, cdsBy, transcriptsBy methods
# =============================================================================

#' Extract and group genomic features from a GTFParquet object
#'
#' @name transcriptsBy
#' @aliases transcriptsBy,GTFParquet-method
#' @aliases exonsBy,GTFParquet-method
#' @aliases cdsBy,GTFParquet-method
#'
#' @description
#' Generic functions to extract genomic features of a given type
#' grouped based on another type of genomic feature.
#' These methods extend the GenomicFeatures generics for GTFParquet objects.
#'
#' @usage
#' \S4method{transcriptsBy}{GTFParquet}(x, by="gene", filter=NULL)
#'
#' \S4method{exonsBy}{GTFParquet}(x, by=c("tx", "gene"), filter=NULL)
#'
#' \S4method{cdsBy}{GTFParquet}(x, by=c("tx", "gene"), filter=NULL)
#'
#' @param x A \code{\link{GTFParquet}} object.
#' @param by One of \code{"gene"}, \code{"tx"} (transcript).
#'   Determines the grouping. For \code{transcriptsBy}, only \code{"gene"}
#'   is currently supported.
#' @param filter Optional named list for filtering features before grouping.
#'   Names should be column names (e.g., \code{gene_type}, \code{chrom}),
#'   values are vectors of acceptable values.
#'   Example: \code{filter = list(gene_type = "protein_coding", chrom = "chr1")}
#'
#' @details
#' These functions return a \link[GenomicRanges]{GRangesList} object
#' where the ranges within each of the elements are ordered according
#' to the following rule:
#'
#' When using \code{exonsBy} or \code{cdsBy} with \code{by = "tx"},
#' the returned exons or CDS are ordered by ascending exon number for each
#' transcript, that is, by their position in the transcript.
#' In all other cases, the ranges will be ordered by chromosome, strand,
#' start, and end values.
#'
#' Unlike TxDb methods, GTFParquet methods preserve rich metadata columns
#' including \code{transcript_name}, \code{transcript_type}, \code{exon_number},
#' \code{protein_id}, and \code{frame}.
#'
#' The \code{filter} argument allows efficient server-side filtering before
#' data is loaded into R, which can dramatically improve performance for
#' large annotation files.
#'
#' @return
#' A \link[GenomicRanges]{GRangesList} object. The names of the list elements
#' are the IDs of the grouping features (gene IDs or transcript IDs).
#'
#' For GTFParquet objects, the names use stripped (unversioned) IDs by default
#' (e.g., \code{ENSG00000141510} rather than \code{ENSG00000141510.18}).
#'
#' @seealso
#' \itemize{
#'   \item \code{\link{GTFParquet-class}} for the class definition
#'   \item \code{\link{genes,GTFParquet-method}} for extracting ungrouped features
#'   \item \code{\link[GenomicFeatures]{transcriptsBy}} for the generic
#'   \item \code{\link[GenomicFeatures]{exonsBy}} for the generic
#'   \item \code{\link[GenomicFeatures]{cdsBy}} for the generic
#' }
#'
#' @examples
#' \dontrun{
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#'
#' # Exons grouped by transcript (sorted by exon_number)
#' ebt <- exonsBy(gtf, by = "tx")
#' ebt[[1]]  # Exons for first transcript
#'
#' # Exons grouped by gene
#' ebg <- exonsBy(gtf, by = "gene")
#'
#' # CDS grouped by transcript
#' cbt <- cdsBy(gtf, by = "tx")
#'
#' # Transcripts grouped by gene
#' tbg <- transcriptsBy(gtf, by = "gene")
#'
#' # Filter to protein-coding only
#' pc_exons <- exonsBy(gtf, by = "tx", 
#'                     filter = list(gene_type = "protein_coding"))
#'
#' # Filter by chromosome
#' chr1_cds <- cdsBy(gtf, by = "tx", filter = list(chrom = "chr1"))
#' }
#'
#' @rdname transcriptsBy
#' @export
setMethod("exonsBy", "GTFParquet", function(x, by = c("tx", "gene"), filter = NULL) {
  by <- match.arg(by)
  
  if (!x@available[["exons"]]) {
    stop("exons.parquet not found")
  }
  
  group_col <- if (by == "tx") "transcript_id_stripped" else "gene_id_stripped"
  
  select_cols <- c("exon_id_stripped", "exon_number", "transcript_id_stripped",
                   "gene_id_stripped", "chrom", "start", "end", "strand")
  
  df <- arrow::read_parquet(x@files$exons, col_select = dplyr::any_of(select_cols))
  
  if (!is.null(filter)) {
    for (col_name in names(filter)) {
      df <- df |> dplyr::filter(.data[[col_name]] %in% filter[[col_name]])
    }
  }
  
  df <- df |> dplyr::arrange(.data[[group_col]], exon_number)
  
  .df_to_grangeslist(df, group_col = group_col, id_col = "exon_id_stripped",
                     mcol_cols = c("exon_number", "transcript_id_stripped", "gene_id_stripped"),
                     genome = x@.genome)
})


