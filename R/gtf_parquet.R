#' GTFParquet class
#' @import methods
#' @import GenomicRanges
#' @import GenomeInfoDb
#' @import GenomicFeatures
#' @import S4Vectors
#' @import IRanges
#' @importFrom arrow read_parquet open_dataset
#' @importFrom dplyr select filter collect arrange any_of pull distinct
#' @description
#' An S4 class for accessing GTF annotations stored in Parquet format.
#' Unlike TxDb, preserves all GTF attributes (gene_type, gene_name, 
#' transcript_support_level, tags, etc.)
#'
#' @details
#' GTFParquet objects are created by the \code{\link{GTFParquet}} constructor
#' function from a directory of Parquet files generated by \code{gtf_to_parquet.py}.
#' 
#' The class implements methods for GenomicFeatures generics including
#' \code{\link[GenomicFeatures]{genes}}, \code{\link[GenomicFeatures]{transcripts}},
#' \code{\link[GenomicFeatures]{exons}}, \code{\link[GenomicFeatures]{cds}},
#' \code{\link[GenomicFeatures]{exonsBy}}, \code{\link[GenomicFeatures]{cdsBy}},
#' and \code{\link[GenomicFeatures]{transcriptsBy}}.
#'
#' All methods support a \code{filter} argument for efficient querying
#' (e.g., \code{filter = list(gene_type = "protein_coding")}).
#'
#' @slot path Character. Path to the Parquet directory.
#' @slot files List. Paths to individual Parquet files.
#' @slot available Logical vector. Which files are present.
#' @slot is_partitioned Logical. Whether genes are partitioned by chromosome.
#' @slot .genome Character. Reference genome build (e.g., "GRCh38").
#'
#' @seealso
#' \itemize{
#'   \item \code{\link{GTFParquet}} for the constructor function
#'   \item \code{\link{genes,GTFParquet-method}} for extracting genes
#'   \item \code{\link{transcriptsBy,GTFParquet-method}} for grouped extractors
#'   \item \code{\link[GenomicFeatures]{TxDb}} for comparison with TxDb objects
#' }
#'
#' @examples
#' \dontrun{
#' # Create from Parquet directory
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#' 
#' # Extract genes with full attributes
#' gr <- genes(gtf)
#' mcols(gr)  # gene_name, gene_type, level, tags, etc.
#' 
#' # Filter by gene type
#' pc <- genes(gtf, filter = list(gene_type = "protein_coding"))
#' }
#'
#' @exportClass GTFParquet
setClass("GTFParquet",
  slots = c(
    path = "character",
    files = "list",
    available = "logical",
    is_partitioned = "logical",
    .genome = "character"
  )
)


#' Create a GTFParquet object
#'
#' @param path Path to directory containing Parquet files from gtf_to_parquet.py
#' @return A GTFParquet S4 object
#' @export
#' @examples
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#' genes(gtf)
#' genes(gtf, filter = list(gene_type = "protein_coding"))
GTFParquet <- function(path) {
  path <- normalizePath(path, mustWork = TRUE)
  
  # Check which files exist
  files <- list(
    genes = file.path(path, "genes.parquet"),
    transcripts = file.path(path, "transcripts.parquet"),
    exons = file.path(path, "exons.parquet"),
    cds = file.path(path, "cds.parquet"),
    features = file.path(path, "features.parquet"),
    metadata = file.path(path, "metadata.parquet")
  )
  
  available <- sapply(files, file.exists)
  
  # Check for partitioned genes directory
  is_partitioned <- dir.exists(file.path(path, "genes"))
  if (is_partitioned) {
    files$genes <- file.path(path, "genes")
    available["genes"] <- TRUE
  }
  
  # Load genome from metadata if available
  genome_val <- NA_character_
  if (available[["metadata"]]) {
    meta <- arrow::read_parquet(files$metadata)
    meta_list <- setNames(meta$value, meta$name)
    if ("genome" %in% names(meta_list)) {
      genome_val <- meta_list[["genome"]]
    }
  }
  
  new("GTFParquet",
      path = path,
      files = files,
      available = available,
      is_partitioned = is_partitioned,
      .genome = genome_val)
}


#' @export
setMethod("show", "GTFParquet", function(object) {
  cat("GTFParquet object\n")
  cat("  Path:", object@path, "\n")
  if (!is.na(object@.genome)) {
    cat("  Genome:", object@.genome, "\n")
  }
  cat("  Available tables:\n")
  for (name in names(object@available)) {
    status <- if (object@available[[name]]) "[x]" else "[ ]"
    cat(sprintf("    %s %s\n", status, name))
  }
  
  # Show metadata if available
  if (object@available[["metadata"]]) {
    meta <- arrow::read_parquet(object@files$metadata)
    cat("  Metadata:\n")
    for (i in seq_len(nrow(meta))) {
      cat(sprintf("    %s: %s\n", meta$name[i], meta$value[i]))
    }
  }
})


