#' Find features overlapping a genomic region
#'
#' @description
#' Efficient region queries that use chromosome-based filtering
#' before computing overlaps.
#'
#' @param x A \code{\link{GTFParquet}} object.
#' @param region A \link[GenomicRanges]{GRanges} object specifying the query region(s).
#' @param ... Additional arguments passed to \code{genes()} or \code{transcripts()}.
#'
#' @return A \link[GenomicRanges]{GRanges} object containing features that
#'   overlap the query region.
#'
#' @details
#' These functions first filter by chromosome (using Parquet predicate pushdown
#' for efficiency), then compute overlaps using \code{findOverlaps}.
#'
#' @examples
#' \dontrun{
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#' 
#' # Define a query region
#' region <- GRanges("chr1", IRanges(1000000, 2000000))
#' 
#' # Find overlapping genes
#' genes_in_region(gtf, region)
#' 
#' # Find overlapping transcripts (protein-coding only)
#' transcripts_in_region(gtf, region, 
#'                       filter = list(transcript_type = "protein_coding"))
#' }
#'
#' @name region-queries
#' @rdname region-queries
#' @export
genes_in_region <- function(x, region, ...) {
  stopifnot(inherits(region, "GRanges"))
  
  chroms <- as.character(unique(seqnames(region)))
  all_genes <- genes(x, filter = list(chrom = chroms), ...)
  
  hits <- findOverlaps(all_genes, region)
  all_genes[queryHits(hits)]
}


#' @rdname region-queries
#' @export
transcripts_in_region <- function(x, region, ...) {
  stopifnot(inherits(region, "GRanges"))
  
  chroms <- as.character(unique(seqnames(region)))
  all_tx <- transcripts(x, filter = list(chrom = chroms), ...)
  
  hits <- findOverlaps(all_tx, region)
  all_tx[queryHits(hits)]
}


