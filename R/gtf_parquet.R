# =============================================================================
# GenomicFeatures::seqinfo method
# =============================================================================

#' Get sequence information from a GTFParquet object
#'
#' @param x A \code{\link{GTFParquet}} object.
#' @return A \link[GenomeInfoDb]{Seqinfo} object containing chromosome names
#'   and genome build.
#' @seealso \code{\link[GenomeInfoDb]{seqinfo}}
#' @rdname GTFParquet-class
#' @export
setMethod("seqinfo", "GTFParquet", function(x) {
  # Build seqinfo from genes table
  if (x@available[["genes"]]) {
    ds <- arrow::open_dataset(x@files$genes)
    chroms <- ds |>
      dplyr::select(chrom) |>
      dplyr::distinct() |>
      dplyr::collect() |>
      dplyr::pull(chrom) |>
      sort()
  } else if (x@available[["transcripts"]]) {
    df <- arrow::read_parquet(x@files$transcripts, col_select = "chrom")
    chroms <- sort(unique(df$chrom))
  } else {
    return(Seqinfo())
  }
  
  si <- Seqinfo(seqnames = chroms)
  if (!is.na(x@.genome)) {
    genome(si) <- x@.genome
  }
  si
})


