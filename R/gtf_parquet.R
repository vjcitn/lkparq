#' Extract UTR and codon features from a GTFParquet object
#'
#' @name utrs
#' @aliases utrs,GTFParquet-method
#' @aliases codons,GTFParquet-method
#'
#' @description
#' Functions to extract UTR (untranslated region) and codon features
#' from a GTFParquet object. These features are stored in the features.parquet
#' file generated by gtf_to_parquet.py.
#'
#' @param x A \code{\link{GTFParquet}} object.
#' @param type For \code{utrs}: one of \code{"both"}, \code{"5prime"}, or \code{"3prime"}.
#'   For \code{codons}: one of \code{"both"}, \code{"start"}, or \code{"stop"}.
#' @param filter Optional named list for filtering features.
#'
#' @return A \link[GenomicRanges]{GRanges} object with metadata columns
#'   including \code{feature_type}, \code{transcript_id}, and \code{gene_id}.
#'
#' @examples
#' \dontrun{
#' gtf <- GTFParquet(system.file("gc49", package="lkparq"))
#' 
#' # 5' UTRs
#' utr5 <- utrs(gtf, type = "5prime")
#' 
#' # 3' UTRs
#' utr3 <- utrs(gtf, type = "3prime")
#' 
#' # Start codons
#' start <- codons(gtf, type = "start")
#' 
#' # Stop codons
#' stop <- codons(gtf, type = "stop")
#' }
#'
#' @rdname utrs
#' @export
setGeneric("utrs", function(x, type = "both", filter = NULL) standardGeneric("utrs"))

#' @rdname utrs
#' @export
setMethod("utrs", "GTFParquet", function(x, type = c("both", "5prime", "3prime"), 
                                          filter = NULL) {
  type <- match.arg(type)
  
  if (!x@available[["features"]]) {
    stop("features.parquet not found - no UTR data available")
  }
  
  df <- arrow::read_parquet(x@files$features)
  
  utr_types <- switch(type,
    "5prime" = c("five_prime_utr", "UTR"),
    "3prime" = c("three_prime_utr"),
    "both" = c("five_prime_utr", "three_prime_utr", "UTR")
  )
  
  df <- df |> dplyr::filter(feature_type %in% utr_types)
  
  if (!is.null(filter)) {
    for (col_name in names(filter)) {
      df <- df |> dplyr::filter(.data[[col_name]] %in% filter[[col_name]])
    }
  }
  
  if (nrow(df) == 0) {
    return(GRanges())
  }
  
  gr <- GRanges(
    seqnames = df$chrom,
    ranges = IRanges(start = df$start, end = df$end),
    strand = df$strand
  )
  
  mcols(gr) <- df[, c("feature_type", "transcript_id", "gene_id"), drop = FALSE]
  
  if (!is.na(x@.genome)) {
    si <- seqinfo(gr)
    genome(si) <- x@.genome
    seqinfo(gr) <- si
  }
  
  gr
})


